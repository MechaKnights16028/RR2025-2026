package org.firstinspires.ftc.teamcode.vision;

/**
 * Represents the color of balls in the 2025 FTC challenge.
 * Used to identify which ball color to target during autonomous.
 */
public enum BallColor {
    /**
     * Purple colored ball
     */
    PURPLE,

    /**
     * Green colored ball
     */
    GREEN,

    /**
     * No ball or unknown color
     */
    NONE
}
